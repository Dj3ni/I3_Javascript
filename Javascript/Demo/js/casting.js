// Casting de Number -> String
let nombreEntier = 10;
console.log('nombreEntier :>> ', typeof(nombreEntier));
nombreEntier = nombreEntier.toString();
console.log('nombreEntier :>> ', typeof(nombreEntier));

// Casting de Float -> String
let nombreReel = 10.5;
console.log('nombreReel :>> ', typeof(nombreReel));
nombreReel = nombreReel.toString();
console.log('nombreReel :>> ', typeof(nombreReel));

// Casting de Boolean -> String
let valeurBool = true;
console.log('valeurBool :>> ', typeof(valeurBool));
valeurBool = valeurBool.toString();
console.log('valeurBool :>> ', typeof(valeurBool));

