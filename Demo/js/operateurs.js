let nombre = 5

// Comparaison sur l'égalité
console.log(nombre == 5); // true
console.log(nombre == "5"); // true: fait la conversion et puis compare
console.log(nombre === "5"); // false: même valeur mais pas même type

// Comparaison sur la différence
console.log(nombre != 5); // false
console.log(nombre != 6); //True